//--INCLUDES--//
#include "chrono.h"

// -----------------------------------------------------------------------------

void checkLeap(int year)
{
	cout << year << ": ";
	if (Chrono::leapyear(year))
		cout << "Yes" << endl;
	else
		cout << "No" << endl;
}

int main()
{
	checkLeap(1696);	//yes
	checkLeap(1991);	//no
	checkLeap(1995);	//no
	checkLeap(2000);	//yes
	checkLeap(2004);	//yes
	checkLeap(2010);	//no
	checkLeap(2016);	//yes
	checkLeap(2020);	//yes

	char ch;
	cin >> ch;
	return 0;
}

